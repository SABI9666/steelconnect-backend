// === CONTRACTOR SHEET UPLOAD - Auto-generates dashboard, sends to admin for approval ===
// Supports: file upload (.xlsx, .xls, .csv) AND/OR Google Sheet link
// POST /api/analysis/upload-sheet
router.post('/upload-sheet', (req, res, next) => {
    upload.single('spreadsheet')(req, res, (err) => {
        if (err) {
            if (err.code === 'LIMIT_FILE_SIZE') {
                return res.status(413).json({ success: false, message: 'File exceeds 50MB limit. Please use a smaller file.' });
            }
            if (err.message && err.message.includes('Unsupported file type')) {
                return res.status(400).json({ success: false, message: err.message });
            }
            return res.status(400).json({ success: false, message: 'File upload failed: ' + (err.message || 'Unknown error') });
        }
        next();
    });
}, async (req, res) => {
    try {
        const userEmail = req.user.email;
        const userName = req.user.name || req.user.displayName || 'Unknown User';

        if (!userEmail) {
            return res.status(400).json({ success: false, message: 'User email not found' });
        }

        const { title, frequency, description, dataType, googleSheetUrl, syncInterval } = req.body;
        const hasFile = !!req.file;
        const hasLink = !!(googleSheetUrl && googleSheetUrl.trim());

        if (!hasFile && !hasLink) {
            return res.status(400).json({ success: false, message: 'Please upload a spreadsheet file (.xlsx, .xls, .csv) or provide a Google Sheet link' });
        }

        let sheets = {};
        let sheetNames = [];
        let dashboardCharts = [];
        let fileName = '';
        let fileSize = 0;
        let chartsGenerated = 0;

        // ── FILE UPLOAD PATH: parse, generate charts, store lightweight in Firestore ──
        if (hasFile) {
            sheets = parseSpreadsheet(req.file.buffer, req.file.originalname);
            sheetNames = Object.keys(sheets);
            fileName = req.file.originalname;
            fileSize = req.file.size;

            if (sheetNames.length === 0) {
                return res.status(400).json({ success: false, message: 'No valid data found in the uploaded file. Please check your spreadsheet.' });
            }

            dashboardCharts = autoGenerateDashboardConfig(sheets, frequency || 'daily');

            if (dashboardCharts.length === 0) {
                return res.status(400).json({ success: false, message: 'Could not extract numeric data from the spreadsheet. Ensure columns have numeric values.' });
            }

            chartsGenerated = dashboardCharts.length;

            // Upload original file to Firebase Storage for safekeeping
            let storagePath = null;
            try {
                storagePath = await uploadSpreadsheetToStorage(req.file.buffer, req.file.originalname, userEmail);
            } catch (e) { /* non-fatal */ }

            // Generate lightweight predictive analysis
            let predictiveForStorage = null;
            try {
                const pa = generatePredictiveAnalysis(sheets, dashboardCharts);
                predictiveForStorage = {
                    forecasts: (pa.forecasts || []).slice(0, 5),
                    correlations: pa.correlations || null,
                    anomalies: (pa.anomalies || []).slice(0, 5),
                    insights: (pa.insights || []).slice(0, 15),
                    movingAverages: {},
                    seasonality: pa.seasonality || null
                };
            } catch (paErr) {
                console.error('[ANALYSIS] Predictive analysis error (non-fatal):', paErr.message);
            }

            let dashboardData = {
                title: title || `Dashboard - ${fileName}`,
                description: description || '',
                dataType: dataType || 'Production Update',
                contractorEmail: userEmail,
                contractorName: userName,
                frequency: frequency || 'daily',
                fileName, fileSize,
                storagePath: storagePath || null,
                googleSheetUrl: null, linkType: null,
                syncInterval: 'manual', lastSyncedAt: null, lastSyncStatus: null,
                sheetNames,
                charts: createLightweightCharts(dashboardCharts, 100),
                totalChartsGenerated: chartsGenerated,
                predictiveAnalysis: predictiveForStorage,
                dataSource: 'file',
                status: 'pending', uploadedBy: 'contractor', createdBy: userEmail,
                createdAt: new Date().toISOString(), updatedAt: new Date().toISOString(),
                approvedAt: null, approvedBy: null
            };

            dashboardData = trimToFit(dashboardData, FIRESTORE_DOC_LIMIT);
            let estimatedSize = estimateJsonSize(dashboardData);
            console.log(`[ANALYSIS] File dashboard size after trim: ~${(estimatedSize / 1024).toFixed(1)}KB`);

            // Safety: if still over limit after trimToFit, aggressively strip data
            if (estimatedSize > FIRESTORE_DOC_LIMIT) {
                console.warn(`[ANALYSIS] Dashboard still over limit (${(estimatedSize / 1024).toFixed(1)}KB), applying emergency trim`);
                dashboardData.charts = (dashboardData.charts || []).slice(0, 2).map(c => ({
                    sheetName: c.sheetName, customTitle: c.customTitle, chartType: c.chartType,
                    labelColumn: c.labelColumn, dataColumns: (c.dataColumns || []).slice(0, 3),
                    labels: [], datasets: [], kpis: [], rowCount: 0, isLightweight: true
                }));
                dashboardData.predictiveAnalysis = null;
                dashboardData.sheetNames = (dashboardData.sheetNames || []).slice(0, 5);
                estimatedSize = estimateJsonSize(dashboardData);
                console.log(`[ANALYSIS] After emergency trim: ~${(estimatedSize / 1024).toFixed(1)}KB`);
            }

            let docRef;
            try {
                docRef = await adminDb.collection('dashboards').add(dashboardData);
            } catch (firestoreErr) {
                // If Firestore still rejects, try one more time with minimal data
                if (firestoreErr.message && (firestoreErr.message.includes('too large') || firestoreErr.message.includes('exceeds') || firestoreErr.message.includes('INVALID_ARGUMENT'))) {
                    console.warn('[ANALYSIS] Firestore save failed, retrying with minimal data');
                    dashboardData.charts = [];
                    dashboardData.predictiveAnalysis = null;
                    docRef = await adminDb.collection('dashboards').add(dashboardData);
                } else {
                    throw firestoreErr;
                }
            }
            console.log(`[ANALYSIS] Contractor ${userEmail} uploaded file -> Dashboard ${docRef.id} (pending)`);

            return res.json({
                success: true,
                message: `Dashboard auto-generated with ${chartsGenerated} charts from uploaded file! Sent to admin for approval.`,
                dashboardId: docRef.id,
                chartsGenerated,
                sheetsProcessed: sheetNames.length
            });
        }

        // ── LINK UPLOAD PATH: validate link, store ONLY metadata (no chart data) ──
        // Sheet data is fetched on-demand via /live-data endpoint to avoid Firestore size limits
        if (hasLink) {
            const trimmedUrl = googleSheetUrl.trim();
            const linkType = detectLinkType(trimmedUrl);
            console.log(`[ANALYSIS] Link upload (${linkType}): ${trimmedUrl.substring(0, 80)}...`);

            // Quick validation: try to fetch the sheet to confirm the link works
            try {
                const result = await fetchSheetData(trimmedUrl);
                sheetNames = result.sheetNames;
                // Count how many charts would be generated (for the response message)
                const previewCharts = autoGenerateDashboardConfig(result.sheets, frequency || 'daily');
                chartsGenerated = previewCharts.length;
                fileName = linkType === 'google' ? 'Google Sheet' : linkType === 'sharepoint' ? 'SharePoint File' : 'Linked Sheet';

                if (chartsGenerated === 0) {
                    return res.status(400).json({ success: false, message: 'Could not extract numeric data from the linked sheet. Ensure it contains columns with numeric values.' });
                }
                console.log(`[ANALYSIS] Link validated: ${sheetNames.length} sheets, ${chartsGenerated} charts possible`);
            } catch (sheetError) {
                console.error('[ANALYSIS] Sheet fetch error:', sheetError.message);
                return res.status(400).json({ success: false, message: sheetError.message });
            }

            // Store ONLY metadata + link in Firestore (tiny document, never exceeds size limit)
            const dashboardData = {
                title: title || `Dashboard - ${fileName}`,
                description: description || '',
                dataType: dataType || 'Production Update',
                contractorEmail: userEmail,
                contractorName: userName,
                frequency: frequency || 'daily',
                fileName, fileSize: 0,
                storagePath: null,
                googleSheetUrl: trimmedUrl,
                linkType,
                syncInterval: syncInterval || 'daily',
                lastSyncedAt: new Date().toISOString(),
                lastSyncStatus: 'success',
                sheetNames,
                charts: [],
                totalChartsGenerated: chartsGenerated,
                predictiveAnalysis: null,
                dataSource: 'link',
                status: 'pending', uploadedBy: 'contractor', createdBy: userEmail,
                createdAt: new Date().toISOString(), updatedAt: new Date().toISOString(),
                approvedAt: null, approvedBy: null
            };

            const docRef = await adminDb.collection('dashboards').add(dashboardData);
            console.log(`[ANALYSIS] Contractor ${userEmail} linked ${linkType} sheet -> Dashboard ${docRef.id} (pending, data on-demand)`);

            const syncMsg = syncInterval !== 'manual' ? ` Auto-sync: ${syncInterval}.` : '';
            return res.json({
                success: true,
                message: `Dashboard linked with ${chartsGenerated} charts from ${fileName}! Sent to admin for approval.${syncMsg}`,
                dashboardId: docRef.id,
                chartsGenerated,
                sheetsProcessed: sheetNames.length
            });
        }

    } catch (error) {
        console.error('[ANALYSIS] Sheet upload error:', error.message, error.stack);
        const msg = (error.message || '').toLowerCase();
        if (msg.includes('exceeds the maximum allowed size') || msg.includes('invalid_argument') || msg.includes('too large') || msg.includes('document size')) {
            res.status(400).json({ success: false, message: 'Your spreadsheet has too many sheets or columns. Try uploading a file with fewer sheets (max 5) or fewer columns.' });
        } else if (msg.includes('cfb') || msg.includes('corrupted') || msg.includes('unsupported') || msg.includes('file type')) {
            res.status(400).json({ success: false, message: 'Could not read this file. Please ensure it is a valid .xlsx, .xls, or .csv file.' });
        } else if (msg.includes('timeout') || msg.includes('abort') || msg.includes('econnreset')) {
            res.status(504).json({ success: false, message: 'The request timed out while processing your data. Try a smaller file or fewer sheets.' });
        } else if (msg.includes('not publicly shared') || msg.includes('requires authentication') || msg.includes('permission')) {
            res.status(400).json({ success: false, message: error.message });
        } else if (msg.includes('payload too large') || msg.includes('entity too large') || msg.includes('request entity')) {
            res.status(413).json({ success: false, message: 'File is too large to upload. Maximum file size is 50MB.' });
        } else {
            res.status(500).json({ success: false, message: 'Failed to process your data. Please try again or upload a smaller file.' });
        }
    }
});

// GET /api/analysis/my-dashboards - Get all dashboards for this contractor (pending + approved + rejected)
