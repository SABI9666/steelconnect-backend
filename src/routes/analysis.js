// GET /api/analysis/dashboard/:id/html-report - Get HTML report with injected data for rendering
router.get('/dashboard/:id/html-report', async (req, res) => {
    try {
        const userEmail = req.user.email;
        const doc = await adminDb.collection('dashboards').doc(req.params.id).get();
        if (!doc.exists) return res.status(404).json({ success: false, message: 'Dashboard not found' });

        const data = doc.data();
        if (data.contractorEmail !== userEmail) {
            return res.status(403).json({ success: false, message: 'Access denied' });
        }
        if (data.status !== 'approved') {
            return res.status(403).json({ success: false, message: 'Dashboard not approved' });
        }
        if (data.reportType !== 'html' || !data.htmlReport) {
            return res.status(400).json({ success: false, message: 'No HTML report available' });
        }

        let htmlContent = data.htmlReport.htmlContent;

        // If no inline content, fetch from storage
        if (!htmlContent && data.htmlReport.storagePath) {
            const bucket = storage.bucket();
            const fileRef = bucket.file(data.htmlReport.storagePath);
            const [fileBuffer] = await fileRef.download();
            htmlContent = fileBuffer.toString('utf-8');
        }

        if (!htmlContent) {
            return res.status(404).json({ success: false, message: 'HTML report content not found' });
        }

        // Inject template data if available (from user's latest data upload)
        let templateData = data.templateData || null;

        // If no template data but has stored spreadsheet, parse it
        if (!templateData && data.storagePath) {
            try {
                const bucket = storage.bucket();
                const fileRef = bucket.file(data.storagePath);
                const [fileBuffer] = await fileRef.download();
                const fileName = data.fileName || 'file.xlsx';
                const sheets = parseSpreadsheet(fileBuffer, fileName);
                templateData = {};
                for (const [name, rows] of Object.entries(sheets)) {
                    templateData[name] = rows.slice(0, 1000);
                }
            } catch (e) {
                console.error('[ANALYSIS] Failed to parse stored spreadsheet for template:', e.message);
            }
        }

        // If no template data but has linked sheet, fetch it
        if (!templateData && data.googleSheetUrl) {
            try {
                const result = await fetchSheetData(data.googleSheetUrl);
                templateData = {};
                for (const [name, rows] of Object.entries(result.sheets)) {
                    templateData[name] = rows.slice(0, 1000);
                }
            } catch (e) {
                console.error('[ANALYSIS] Failed to fetch sheet for template:', e.message);
            }
        }

        // Inject data into HTML template via a script tag
        if (templateData) {
            const dataScript = `<script>window.__REPORT_DATA__ = ${JSON.stringify(templateData)}; window.__REPORT_META__ = ${JSON.stringify({ title: data.title, frequency: data.frequency, updatedAt: data.templateDataUpdatedAt || data.updatedAt })};</script>`;
            // Insert before </head> or at the start of <body> or at the beginning
            if (htmlContent.includes('</head>')) {
                htmlContent = htmlContent.replace('</head>', dataScript + '</head>');
            } else if (htmlContent.includes('<body')) {
                htmlContent = htmlContent.replace(/<body[^>]*>/, (match) => match + dataScript);
            } else {
                htmlContent = dataScript + htmlContent;
            }
        }

        res.json({
            success: true,
            htmlContent,
            templateData: templateData ? true : false,
            title: data.title,
            lastDataUpdate: data.templateDataUpdatedAt || data.updatedAt
        });
    } catch (error) {
        console.error('[ANALYSIS] HTML report error:', error);
        res.status(500).json({ success: false, message: 'Failed to get HTML report' });
    }
});

